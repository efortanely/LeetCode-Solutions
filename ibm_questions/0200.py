from typing import List
from collections import deque

class Solution:
    def numIslands(self, grid: List[List[str]]) -> int:
        islands = 0
        queue = deque()
        dir = [(0, 1), (0, -1), (-1, 0), (1, 0)]

        for i in range(len(grid)):
            for j in range(len(grid[i])):
                if grid[i][j] == "1":
                    islands += 1
                    queue.append((i, j))
                    grid[i][j] = "0"

                    while queue:
                        row, col = queue.popleft()

                        for move in dir:
                            nbr = (row + move[0], col + move[1])

                            if nbr[0] < 0 or nbr[1] < 0 or nbr[0] >= len(grid) or nbr[1] >= len(grid[0]) or grid[nbr[0]][nbr[1]] == "0":
                                continue

                            queue.append(nbr)
                            grid[nbr[0]][nbr[1]] = "0"

        return islands

